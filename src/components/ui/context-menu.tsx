export const menuContentStyles = cn(
  baseMenuContentStyles,
  "animate-in fade-in-0 zoom-in-95 data-[side=top]:slide-in-from-bottom-2 data-[side=bottom]:slide-in-from-top-2 data-[side=left]:slide-in-from-right-2 data-[side=right]:slide-in-from-left-2",
);

export const menuItemStyles = cn(baseMenuItemStyles, "w-full");

export const menuItemDestructiveStyles = cn(
  menuItemStyles,
  baseMenuItemDestructiveStyles,
);

export const StandaloneMenuContent = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ children, className, ...props }, ref) => {
  return (
    <div ref={ref} className={cn(menuContentStyles, className)} {...props}>
      {children}
    </div>
  );
});
StandaloneMenuContent.displayName = "StandaloneMenuContent";

export function StandaloneMenuItem({
  children,
  className,
  variant = "default",
  disabled = false,
  ...props
}: React.ButtonHTMLAttributes<HTMLButtonElement> & {
  variant?: "default" | "destructive";
}) {
  return (
    <button
      className={cn(
        variant === "destructive" ? menuItemDestructiveStyles : menuItemStyles,
        disabled && "cursor-not-allowed opacity-50",
        className,
      )}
      disabled={disabled}
      {...props}
    >
      {children}
    </button>
  );
}

export function StandaloneMenuSeparator({
  className,
  ...props
}: React.HTMLAttributes<HTMLDivElement>) {
  return (
    <div className={cn("-mx-1 my-1 h-px bg-white/20", className)} {...props} />
  );
}
