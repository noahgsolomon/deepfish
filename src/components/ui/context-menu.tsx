function ContextMenuItem({
  className,
  inset,
  variant = "default",
  ...props
}: React.ComponentProps<typeof ContextMenuPrimitive.Item> & {
  inset?: boolean;
  variant?: "default" | "destructive";
}) {
  return (
    <ContextMenuPrimitive.Item
      data-slot="context-menu-item"
      data-inset={inset}
      data-variant={variant}
      className={cn(
        baseMenuItemStyles,
        variant === "destructive" && baseMenuItemDestructiveStyles,
        "data-[inset]:pl-8",
        className,
      )}
      {...props}
    />
  );
}

