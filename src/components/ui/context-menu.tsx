// Base style constants for sharing between components
const baseMenuContentStyles =
  "z-50 min-w-[8rem] overflow-hidden rounded-none border border-border-default bg-surface-primary p-1 text-white font-mono";
const baseMenuItemStyles =
  "relative flex cursor-default select-none items-center gap-2 rounded-none px-2 py-1.5 text-xs outline-none font-mono hover:bg-white/5 hover:text-white focus:bg-white/5 focus:text-white transition-colors data-[disabled]:pointer-events-none data-[disabled]:opacity-50 [&_svg]:pointer-events-none [&_svg]:shrink-0 [&_svg:not([class*='size-'])]:size-4 [&_svg:not([class*='text-'])]:text-text-secondary";
const baseMenuItemDestructiveStyles =
  "text-red-400 hover:bg-red-500/10 focus:bg-red-500/10 hover:text-red-400 focus:text-red-400 [&_svg]:!text-red-400";

